class MinStack {
        Deque<Integer> stack;
        int minVal;
    /** initialize your data structure here. */
    public MinStack() {
        stack = new LinkedList<>();
        minVal = Integer.MAX_VALUE;
    }

    public void push(int val) {
        stack.push(val);
        minVal = Math.min(minVal, val);
    }

    public void pop() {
        int pop = stack.pop();
        if(pop == minVal) {
            minVal = Integer.MAX_VALUE;
            Iterator<Integer> it = stack.iterator();
            while (it.hasNext()) {
                minVal = Math.min(minVal, it.next());
            }
        }
    }

    public int top() {
        return stack.peek();
    }

    public int getMin() {
        return minVal;
    }
}

