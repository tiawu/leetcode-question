class KthLargest {
    PriorityQueue<Integer> pqueue;
    int k;

    public KthLargest(int k, int[] nums) {
        this.k = k;
        pqueue = new PriorityQueue<>(new Comparator<Integer>() {
            @Override
            // é‡‡ç”¨å°æ ¹å †
            public int compare(Integer o1, Integer o2) {
                return o1 - o2;
            }
        });
        for(int num : nums) {
            add(num);
        }

    }
    
    public int add(int val) {
        pqueue.offer(val);
        if(pqueue.size() > k) {
            pqueue.poll();
        }
        return pqueue.peek();
    }
}

